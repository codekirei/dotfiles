vim.o.wrap = false

-- show both current line number and relative line numbers
vim.o.number = true
vim.o.relativenumber = true

-- highlight line num of cursor line
vim.o.cursorline = true
vim.o.cursorlineopt = 'number'

-- maintain 12 lines of margin at edges if possible
vim.opt.scrolloff = 12

-- always show sign column with fixed width
vim.opt.signcolumn = "yes:1"
